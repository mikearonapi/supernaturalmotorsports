/**
 * Helper to generate a URL-friendly slug from a car name
 * @param {string} name - The car name
 * @returns {string} - URL-friendly slug
 */
export function generateSlug(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

