/**
 * Get all unique categories (chassis layouts) from car data
 * @returns {string[]}
 */
export function getUniqueCategories() {
  return [...new Set(carData.map(car => car.category))];
}

/**
 * Get cars by tier
 * @param {string} tier
 * @returns {Car[]}
 */
export function getCarsByTier(tier) {
  return carData.filter(car => car.tier === tier);
}

/**
 * Get a car by its slug
 * @param {string} slug
 * @returns {Car|undefined}
 */
export function getCarBySlug(slug) {
  return carData.find(car => car.slug === slug);
}

/**
 * Get a car by its ID
 * @param {number} id
 * @returns {Car|undefined}
 */
export function getCarById(id) {
  return carData.find(car => car.id === id);
}

