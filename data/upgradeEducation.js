/**
 * Get upgrades by category
 * @param {string} categoryKey - The category key
 * @returns {Object[]} Array of upgrade objects in that category
 */
export function getUpgradesByCategory(categoryKey) {
  return Object.values(upgradeDetails).filter(u => u.category === categoryKey);
}

/**
 * Get all categories with their upgrades
 * @returns {Object} Categories with nested upgrades
 */
export function getAllUpgradesGrouped() {
  const grouped = {};
  Object.keys(upgradeCategories).forEach(catKey => {
    grouped[catKey] = {
      ...upgradeCategories[catKey],
      upgrades: getUpgradesByCategory(catKey),
    };
  });
  return grouped;
}

/**
 * Get a single upgrade by key
 * @param {string} key - The upgrade key
 * @returns {Object|null} The upgrade object or null
 */
export function getUpgradeDetail(key) {
  return upgradeDetails[key] || null;
}

/**
 * Get all upgrade keys
 * @returns {string[]} Array of all upgrade keys
 */
export function getAllUpgradeKeys() {
  return Object.keys(upgradeDetails);
}

/**
 * Search upgrades by name or description
 * @param {string} query - Search query
 * @returns {Object[]} Matching upgrades
 */
export function searchUpgrades(query) {
  const lowerQuery = query.toLowerCase();
  return Object.values(upgradeDetails).filter(u => 
    u.name.toLowerCase().includes(lowerQuery) ||
    u.shortDescription.toLowerCase().includes(lowerQuery) ||
    u.category.toLowerCase().includes(lowerQuery)
  );
}

